package org.joyqueue.nsr.sql.domain;

/**
 * DataCenterDTO
 * author: gaohaoxiang
 * date: 2019/8/16
 */
public class DataCenterDTO extends BaseDTO {

    private String id;
    private String region;
    private String code;
    private String name;
    private String url;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}